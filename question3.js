const decodeMorse = morse => {
    let words = morse.split('   ');
    let decoded = [];

    words.forEach(word => {
        let letters = word.split(' ');
        let decodedWord = '';

        letters.forEach(letter => {
            letter = letter.trim();

            decodedWord += MORSE_CODE[letter];
        });

        decoded.push(decodedWord);
    });

    return decoded.join(' ');
}

